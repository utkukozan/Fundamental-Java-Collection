public class Modifiers {
    public static void main(String[] args) {
        // In Java, access modifiers are used to determine the accessibility of a method or class.
        // Public: Can be used by other classes.
        // Default: Can be used by other classes, but they must be in the same package.
        // Private: Can only be used within the class it is declared in.
        // Protected: Can be used within the same package and by subclasses.
        // Similarly, non-access modifiers are used for methods and classes that are not accessible.
        // Final: Cannot be overridden by another class.
        // Static: Allows accessing methods without creating an object.
        // Abstract: Used to inherit a method from another class.
    }
}
