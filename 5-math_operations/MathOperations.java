public class MathOperations {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        /* Maximum Number using Math.max() */
        System.out.println(Math.max(a, b));
        /* Minimum Number using Math.min() */
        System.out.println(Math.min(a, b));
        /* Square Root using Math.sqrt() */
        System.out.println(Math.sqrt(b));
        /* Absolute Value using Math.abs() */
        System.out.println(Math.abs(-5.2));
        /* Random number between 0.0 and 1.0 using Math.random() */
        System.out.println(Math.random());
        /* Random number between 0 and 100 using Math.random() */
        System.out.println(Math.random() * 101);
    }
}
